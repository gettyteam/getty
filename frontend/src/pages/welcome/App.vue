<script setup>
import { onBeforeUnmount, onMounted } from 'vue';
import { useTheme } from '../../composables/useTheme';

const { isDark, toggleTheme } = useTheme();

const bodyClasses = [
  'bg-background',
  'text-gray-100',
  'font-sans',
  'w-full',
  'm-0',
  'p-0',
  'min-h-screen',
];
const viewportVar = '--welcome-vh';
let previousBodyOverflow = '';
let previousDocOverflow = '';

function updateViewportHeight() {
  const doc = document.documentElement;
  const visualHeight = Math.min(window.innerHeight, doc.clientHeight || window.innerHeight);
  const unit = visualHeight * 0.01;
  document.documentElement.style.setProperty(viewportVar, `${unit}px`);
}

function notifyLegacyBridge() {
  const root = document.getElementById('app-root');
  window.dispatchEvent(
    new CustomEvent('getty-welcome-vue-ready', {
      detail: { root },
    })
  );
  if (typeof window.__GETTY_WELCOME_VUE_READY__ === 'function') {
    try {
      window.__GETTY_WELCOME_VUE_READY__();
    } catch (error) {
      console.warn('[welcome] failed to notify legacy bridge', error);
    }
  }
}

onMounted(() => {
  updateViewportHeight();
  window.addEventListener('resize', updateViewportHeight);
  const doc = document.documentElement;
  doc.classList.add('bg-background');
  previousDocOverflow = doc.style.overflowY;
  previousBodyOverflow = document.body.style.overflow;
  doc.style.overflowY = 'hidden';
  document.body.style.overflow = 'hidden';
  bodyClasses.forEach((className) => {
    if (!document.body.classList.contains(className)) {
      document.body.classList.add(className);
    }
  });
  notifyLegacyBridge();
});

onBeforeUnmount(() => {
  window.removeEventListener('resize', updateViewportHeight);
  document.documentElement.style.removeProperty(viewportVar);
  document.documentElement.classList.remove('bg-background');
  const doc = document.documentElement;
  doc.style.overflowY = previousDocOverflow;
  document.body.style.overflow = previousBodyOverflow;
  bodyClasses.forEach((className) => {
    document.body.classList.remove(className);
  });
});
</script>

